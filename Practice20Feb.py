b=30
c=20
print(a+b+c) # '+' addition
print(a-c) # '-' subtraction
print(a*b) # '*' multiplication
print(a/b) # '/' operator gives float value
print(a//b) # '//' operator gives integer value 
print(a%b) # '%' operator gives the remainder of two operands
print(2**4) # '**' do the power of two operands  

